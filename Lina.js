document.addEventListener("DOMContentLoaded", function() {
    var rectangle1 = document.querySelector(".rectangle1");
    var rect1 = document.querySelector(".rect-1");
    rectangle1.addEventListener("mouseenter", function() {
        rect1.classList.add("show");
    });
    rectangle1.addEventListener("mouseleave", function() {
        rect1.classList.remove("show");
    });
});




